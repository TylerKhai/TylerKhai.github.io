function isFriend(name, object) {
    if(Array.isArray(object.friends) && object.hasOwnProperty('friends')) {
   for(var i = 0; i < object.friends.length; i++) {
     if(name === object.friends[i]) {
       return true;  
     }
   }    
 }  
return false;  
}

